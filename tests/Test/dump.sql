CREATE DATABASE garden;
    
USE garden;

-- Original pet table
CREATE TABLE pet (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, name VARCHAR(20), owner VARCHAR(20), species VARCHAR(20), sex CHAR(1), birth DATE, death DATE, timeUpdated datetime);

-- Numeric data types
CREATE TABLE tinyint_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value TINYINT);
CREATE TABLE smallint_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value SMALLINT);
CREATE TABLE mediumint_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value MEDIUMINT);
CREATE TABLE int_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value INT);
CREATE TABLE bigint_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value BIGINT);
CREATE TABLE decimal_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value DECIMAL(10,2));
CREATE TABLE float_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value FLOAT);
CREATE TABLE double_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value DOUBLE);
CREATE TABLE bit_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value BIT(8));

-- Date and Time data types
CREATE TABLE date_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value DATE);
CREATE TABLE time_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value TIME);
CREATE TABLE datetime_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value DATETIME);
CREATE TABLE timestamp_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value TIMESTAMP);
CREATE TABLE year_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value YEAR);

-- String data types
CREATE TABLE char_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value CHAR(50));
CREATE TABLE varchar_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value VARCHAR(255));
CREATE TABLE binary_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value BINARY(50));
CREATE TABLE varbinary_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value VARBINARY(255));
CREATE TABLE tinytext_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value TINYTEXT);
CREATE TABLE text_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value TEXT);
CREATE TABLE mediumtext_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value MEDIUMTEXT);
CREATE TABLE longtext_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value LONGTEXT);
CREATE TABLE tinyblob_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value TINYBLOB);
CREATE TABLE blob_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value BLOB);
CREATE TABLE mediumblob_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value MEDIUMBLOB);
CREATE TABLE longblob_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value LONGBLOB);
CREATE TABLE enum_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value ENUM('SMALL', 'MEDIUM', 'LARGE'));
CREATE TABLE set_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value SET('RED', 'BLUE', 'GREEN'));

-- JSON data type (MySQL 5.7.8 and later)
CREATE TABLE json_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value JSON);

-- Spatial data types
CREATE TABLE geometry_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value GEOMETRY);
CREATE TABLE point_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value POINT);
CREATE TABLE linestring_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value LINESTRING);
CREATE TABLE polygon_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value POLYGON);
CREATE TABLE multipoint_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value MULTIPOINT);
CREATE TABLE multilinestring_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value MULTILINESTRING);
CREATE TABLE multipolygon_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value MULTIPOLYGON);
CREATE TABLE geometrycollection_table (id INT NOT NULL AUTO_INCREMENT PRIMARY KEY, value GEOMETRYCOLLECTION);
